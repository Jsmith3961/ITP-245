using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Week4ObjectandLoops
{
    internal class Food
    {
        int fID;
        double cost;
        string name, description;

        public int FoodID


        { 
            get { return fID; } 
            set { fID = value; }
        } 

        public double Cost

        { 
            get { return cost; }
            set { cost = value; } 
        }
        
        public string Name

        { get { return name; } 
          set { name = value; }

        
        
        }
                

        public string Description

        {    
            get { return description; }
            set { description = value; } 
        
        }

            
            

    }
    

     
}
